#include <unistd.h>

void	ft_putchar(char c);

int		ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	return (i);
}

int		main(int argc, char **argv)
{
	if (argc)
		ft_putstr(argv[0]);
}

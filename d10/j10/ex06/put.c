int	putadd(int a, int b)
{
	return (a + b);
}

int	putre(int a, int b)
{
	return (a - b);
}

int	putmult(int a, int b)
{
	return (a * b);
}

int	putdiv(int a, int b)
{
	return (a / b);
}

int	putmod(int a, int b)
{
	return (a % b);
}
